import { execFileSync } from 'node:child_process'
import { existsSync, mkdirSync } from 'node:fs'
import path from 'node:path'

const OPENAPI_URL = process.env.OPENAPI_URL || 'http://localhost:6382/api/schema/'
const OUT_FILE = process.env.OPENAPI_OUT || 'src/api/generated.ts'

// ensure output dir exists
const outDir = path.dirname(OUT_FILE)
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })

try {
  // Use the local binary installed in node_modules
  const bin = path.resolve('node_modules/.bin/openapi-typescript')

  execFileSync(
    bin,
    [
      OPENAPI_URL,
      '--output',
      OUT_FILE,
      '--header',
      `// AUTO-GENERATED by openapi-typescript from ${OPENAPI_URL}\n`,
    ],
    { stdio: 'inherit' },
  )

  console.log(`[gen:api] Wrote ${OUT_FILE}`)
} catch (err) {
  console.error(`[gen:api] Failed to generate from ${OPENAPI_URL}`)
  process.exit(1)
}
